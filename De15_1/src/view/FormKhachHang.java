    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        if(list.size()>0){
            KhachHang.setSma(list.get(list.size()-1).getMa()+1);
            
        }
        jTextField1.setText(KhachHang.getSma()+"");
        jTextField2.requestFocus();
        String ten,dc,loaiKH;
        try{
            int ma=Integer.parseInt(jTextField1.getText());
            ten=jTextField2.getText();
            dc=jTextField3.getText();
            loaiKH=jComboBox1.getSelectedItem().toString();
            if(ten.isEmpty() || dc.isEmpty()) throw new TrongException();
            KhachHang kh=new KhachHang(ma,ten,dc,loaiKH);
            tm.addRow(kh.toObjects());
            list.add(kh);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Nhap so");
            
            return;
        }catch(TrongException e){
            JOptionPane.showMessageDialog(this, "Không để trống");
            jTextField2.requestFocus();
            return;
        }
        
    }//GEN-LAST:event_btThemActionPerformed

    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        int row=jTable1.getSelectedRow();
        if(row>=0&& row<jTable1.getColumnCount()){
            tm.setValueAt(jTextField1.getText(), row, 0);
            tm.setValueAt(jTextField2.getText(), row, 1);
            tm.setValueAt(jTextField3.getText(), row, 2);
            tm.setValueAt(jComboBox1.getSelectedItem(), row, 3);
        }
        else{
            JOptionPane.showMessageDialog(this, "Chon de sua");
            return;
        }
            
    }//GEN-LAST:event_btSuaActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row=jTable1.getSelectedRow();
        if(row>=0&& row<jTable1.getColumnCount()){
            jTextField1.setText(jTable1.getValueAt(row, 0).toString());
            jTextField2.setText(jTable1.getValueAt(row, 1).toString());
            jTextField3.setText(jTable1.getValueAt(row, 2).toString());
            jComboBox1.setSelectedItem(jTable1.getValueAt(row, 3).toString());
        }
        else{
            JOptionPane.showMessageDialog(this, "Chon de sua");
            return;
        }
    }//GEN-LAST:event_jTable1MouseClicked

