    private void loadData() {
        File f=new File(file);
        if(f.exists())
            list=IOFile.doc(file);
        else
            list=new ArrayList<>();
                    
            
    }
    

    private void showData() {
        loadData();
        tm.setRowCount(0);
        for(KhachHang i:list)
            tm.addRow(i.toObjects());
    }
