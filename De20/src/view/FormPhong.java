    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        File f = new File(file);
        if(f.exists()&&!ok) {
            ok=true;
            List<Phong> list=IOFile.doc(file);
            if(list.size()>0){
                Phong.setSma(list.get(list.size()-1).getMa()+1);
            }
        }
        jTextField1.setText(Phong.getSma()+"");
        jTextField2.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Phong k = new Phong();
        k.setTen(jTextField2.getText());
        k.setLoai(jComboBox1.getSelectedItem().toString());
        tm.addRow(k.toObject());
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        List<Phong> list=new ArrayList<>();
        for (int i = 0; i < tm.getRowCount(); i++) {
            int ma=Integer.parseInt(tm.getValueAt(i, 0).toString());
            String ten=tm.getValueAt(i, 1).toString();
            String loai=tm.getValueAt(i, 2).toString();
            list.add(new Phong(ma, ten, loai));
        }
        IOFile.viet(file, list);
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        List<Phong> list=IOFile.doc(file);
        tm.setRowCount(0);
        for(Phong i:list) {
            tm.addRow(i.toObject());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int r=jTable1.getSelectedRow();
        if(r<0||r>=tm.getRowCount()){
            JOptionPane.showMessageDialog(this, "Chon dong de xoa!");
        }
        else {
            tm.setValueAt(jTextField2.getText(), r, 1);
            tm.setValueAt(jComboBox1.getSelectedItem().toString(), r, 2);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int r=jTable1.getSelectedRow();
        jTextField1.setText(tm.getValueAt(r, 0).toString());
        jTextField1.setText(tm.getValueAt(r, 1).toString());
        jComboBox1.setSelectedItem(tm.getValueAt(r, 2).toString());
    }//GEN-LAST:event_jTable1MouseClicked


