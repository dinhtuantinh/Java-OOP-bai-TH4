    private void btCnhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCnhatActionPerformed
        // TODO add your handling code here:
        if(them){
            String hoten, dc;
            try{
               hoten=jTextField2.getText();
               dc=jTextField3.getText();
               if(hoten.isEmpty()|| dc.isEmpty()) throw new TrongException();
               int ma=Integer.parseInt(jTextField1.getText());
               KhachHang b=new KhachHang(ma,hoten,dc,jComboBox1.getSelectedItem().toString());
               tm.addRow(b.toObjects());
               lkhachhang.add(b); 
               buttonSate(true);
                them=false;
            }catch(TrongException e){
                JOptionPane.showMessageDialog(this, "Khong de trong!!!");
                jTextField2.requestFocus();
            }
            
        }
        if(sua){
            int row=jTable1.getSelectedRow();
            String hoten ,dc;
            hoten=jTextField2.getText();
            dc=jTextField3.getText();
            int ma=Integer.parseInt(jTextField1.getText());
            KhachHang b=new KhachHang(ma, hoten, dc, jComboBox1.getSelectedItem().toString());
            tm.setValueAt(ma, row, 0);
            tm.setValueAt(hoten, row, 1);
            tm.setValueAt(dc, row, 2);
            lkhachhang.set(row, b);
            sua=false;
            buttonSate(true);
        }
    }//GEN-LAST:event_btCnhatActionPerformed

    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        buttonSate(false);
        them=true;
        if(lkhachhang!=null && lkhachhang.size()>0)
            KhachHang.setSma(lkhachhang.get(lkhachhang.size()-1).getMa()+1);
        else
            lkhachhang=new ArrayList<>();
        jTextField1.setText(KhachHang.getSma()+"");
        jTextField2.requestFocus();
    }//GEN-LAST:event_btThemActionPerformed

    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        int row=jTable1.getSelectedRow();
        if(row<0 || row>jTable1.getRowCount()-1)
            JOptionPane.showMessageDialog(this, "Chon khach hang de sua");
        else{
            sua=true;
            buttonSate(false);
            jTextField2.requestFocus();
        }
    }//GEN-LAST:event_btSuaActionPerformed

    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        // TODO add your handling code here:
        int row=jTable1.getSelectedRow();
        if(row<0 && row>jTable1.getRowCount()-1)
            JOptionPane.showMessageDialog(this, "Chon khach hang de xoa");
        else{
            tm.removeRow(row);
            lkhachhang.remove(row);
        }
    }//GEN-LAST:event_btXoaActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row=jTable1.getSelectedRow();
        if(row>=0 && row<jTable1.getRowCount()){
            jTextField1.setText(tm.getValueAt(row,0).toString());
            jTextField2.setText(tm.getValueAt(row,1).toString());
            jTextField3.setText(tm.getValueAt(row,2).toString());           
        }
    }//GEN-LAST:event_jTable1MouseClicked

