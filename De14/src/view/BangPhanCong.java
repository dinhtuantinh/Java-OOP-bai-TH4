    private void loadData(){
        File f=new File(fbangPC);
        if(f.exists()){
           lbangPC=IOFile.doc(fbangPC);
           }
        else{
            lbangPC = new ArrayList<>();
        }
        File fs=new File(flx);
        if(fs.exists()){
           llx=IOFile.doc(flx);
           }
        else{
            llx= new ArrayList<>();
        }
    }
    private void load2Ma() {
       jComboBox1.removeAllItems();
       jComboBox2.removeAllItems();
      
       String ftuyen="src/controller/TUYEN.dat";
       List<Tuyen> l1=IOFile.doc(ftuyen);
       if(l1!=null){
           for(Tuyen i:l1)
               jComboBox2.addItem(i.getMa()+"");
       }
      
        for(LaiXe i:llx)
             jComboBox1.addItem(i.getMa()+"");   
    }
    private boolean trung2Ma(int maLX, int maTuyen){
        for(PhanCong i:lbangPC)
            if(i.getLx().getMa()==maLX && i.getTuyen().getMa()==maTuyen)
                return true;
        return false;
    }
    private LaiXe getLaiXeByMa(int ma){
      
           for(LaiXe i:llx)
               if(i.getMa()==ma)
                   return i;
       
       return null;
       }
    private Tuyen getTuyenByMa(int ma){
         String fTuyen="src/controller/TUYEN.dat";
         List<Tuyen> l1=IOFile.doc(fTuyen);
       if(l1!=null){
           for(Tuyen i:l1)
               if(i.getMa()==ma)
                   return i;
       }
       return null;
    }
