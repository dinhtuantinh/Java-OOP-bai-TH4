    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        int maLX=Integer.parseInt(jComboBox1.getSelectedItem().toString());
        int maT=Integer.parseInt(jComboBox2.getSelectedItem().toString());
        
        if(trung2Ma(maLX, maT)){
            JOptionPane.showMessageDialog(this, "Trung 2 ma");
            jComboBox1.requestFocus();
            return;
            }
        try{
            int sl=Integer.parseInt(jTextField1.getText());
            PhanCong pc=new PhanCong(getLaiXeByMa(maLX),getTuyenByMa(maT),sl);
            tm.addRow(pc.toObject());
            lbangPC.add(pc);
        //    IOFile.viet(flx, llx);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Không để trống");
            jTextField1.requestFocus();
            return;
        }
            
    }//GEN-LAST:event_btThemActionPerformed

