    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        int manv=Integer.parseInt(jComboBox1.getSelectedItem().toString());
        int mamh=Integer.parseInt(jComboBox2.getSelectedItem().toString());
        
        if(trung2ma(manv, mamh)){
            JOptionPane.showMessageDialog(this, "Trung 2 ma");
            return;
        }
        try{
            int sl=Integer.parseInt(jTextField5.getText());
            DSBH ds=new DSBH(getNVbyMa(manv),getMHbyMa(mamh),sl);
            tm.addRow(ds.toObjects());
            list.add(ds);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Nhap so");
            return;
        }
        
    }//GEN-LAST:event_btThemActionPerformed

