    private void loadData(){
        File f=new File(file);
        if(f.exists()){
            list=IOFile.doc(file);
        }
        else
            list=new ArrayList<>();
    }
    private void load2ma(){
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        
        String fnv="src/controller/NV.dat";
        List<NVBanHang> lnv=IOFile.doc(fnv);
        if(lnv!=null){
            for(NVBanHang i:lnv)
                jComboBox1.addItem(i.getMa()+"");
        }
        
        String fmh="src/controller/MH.dat";
        List<MatHang> lmh=IOFile.doc(fmh);
        if(lmh!=null){
            for(MatHang i:lmh)
                jComboBox2.addItem(i.getMa()+"");
        }
    }
    private boolean trung2ma(int manv,int mamh){
        for(DSBH i:list){
            if(i.getNv().getMa()==manv && i.getMh().getMa()==mamh)
                return true;
        }
        return false;
    }
    private NVBanHang getNVbyMa(int manv){
        String fnv="src/controller/NV.dat";
        List<NVBanHang> lnv=IOFile.doc(fnv);
        if(lnv!=null){
            for(NVBanHang i:lnv)
                if(i.getMa()==manv)
                    return i;
        }
        return null;
    }
    private MatHang getMHbyMa(int mamh){
        String fmh="src/controller/MH.dat";
        List<MatHang> lmh=IOFile.doc(fmh);
        if(lmh!=null){
            for(MatHang i:lmh)
                if(i.getMa()==mamh)
                    return i;
        }
        return null;
    }
